
package EntidadC;


public class Atributos {
 private int Base;
 private int Altura;
 
 public Atributos(int Base, int Altura){
     this.Altura = Altura;
     this.Base = Base;
 }
 
 public Atributos(){
     
 }

    public int getBase() {
        return Base;
    }

    public void setBase(int Base) {
        this.Base = Base;
    }

    public int getAltura() {
        return Altura;
    }

    public void setAltura(int Altura) {
        this.Altura = Altura;
    }
 
 
}
