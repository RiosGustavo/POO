
package EntidadesArrays;

/**
 *
 * @author portatil
 */
public class AtributosArrays {
    private int n;
    private int m;

    public AtributosArrays(int n, int m) {
        this.n = n;
        this.m = m;
    }

   
    
    public AtributosArrays(){
        
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }

   
    
    
}
